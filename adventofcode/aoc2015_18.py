STEPS = 100

import numpy as np

STR_TO_INT = '.#'

data = [[STR_TO_INT.index(c) for c in line] for line in data.splitlines()]
data = np.array(data)
#print(data)
grid = np.zeros((data.shape[0]+2, data.shape[1]+2))
grid[1:data.shape[0]+1, 1:data.shape[1]+1] = data
#print(grid)
#print(grid.shape)

stuck_lights = ((0, 0), (0, grid.shape[1]-3), (grid.shape[0]-3, 0), (grid.shape[0]-3, grid.shape[1]-3))

def change(square):
    #print(square)
    middle = square[1,1]
    #print(middle)
    return np.sum(square) in (3, 4) if middle else np.sum(square) == 3

for step in range(1, STEPS+1):
    print(step)
    copygrid = grid.copy()
    for x in range(data.shape[0]):
        for y in range(data.shape[1]):
            if (x, y) in stuck_lights:
                continue
            square = grid[x:x+3, y:y+3]
            copygrid[x+1,y+1] = change(square)
    grid = copygrid
print(np.sum(grid))
